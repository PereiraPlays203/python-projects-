a = input('digite algo')
print('o tipo de {} é'.format(a), type(a))
print('é letra ?', a.isalpha())
print('é número ?', a.isnumeric())
print('é maiúsculo ?', a.isupper())
print('é minusculo ?', a.islower())
print('é alfa numérico ?', a.isalnum())
print('tem apenas espaço ?', a.isspace())
print('está capitalizada ?', a.istitle())




